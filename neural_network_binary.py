def generate_filter(start_angle=0):
    elips = []
    for i in range(0, 8):
        w = 32
        img=np.zeros((w,w))
        radius=14
        axes = (radius,radius)
        angle = 0
        startAngle = start_angle
        endAngle = start_angle + 180
        center = (16,16)
        color = 255
        elip = cv2.ellipse(img, center, axes, angle, startAngle, endAngle, color, thickness=-1)
        elips.append(elip)
        start_angle += 45
    return np.array(elips)
  
